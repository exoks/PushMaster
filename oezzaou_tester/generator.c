//====<[ random_arr: ]>=========================================================
int	*random_arr(int min, int max, int size)
{
	int	*pool, *arr;
	int	i, j, max_step, tmp;

	if (max - min < size)
		return (0);
	pool = malloc((max - min) * sizeof(int));
	i = min - 1;
	while (++i < max)
		pool[i - min] = i;

	srand(time(NULL));
	i = -1;
	while (++i < max - min)
	{
		j = i + rand() % (max - min - i);
		tmp = pool[j];
		pool[j] = pool[i];
		pool[i] = tmp;
	}
	arr = malloc(size * sizeof(int));
	i = -1;
	while (++i < size)
		arr[i] = pool[i];
	for (int i = 0; i < size; i++)
		printf("%3d ", arr[i]);
	free(arr);
	free(pool);
	return (EXIT_SUCCESS);
}

